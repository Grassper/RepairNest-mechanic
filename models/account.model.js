class Account {
  constructor(
    id,
    profileName,
    profileUrl,
    phoneNumber,
    email,
    birthday,
    gender
  ) {
    this.id = id;
    this.profileName = profileName;
    this.profileUrl = profileUrl;
    this.phoneNumber = phoneNumber;
    this.email = email;
    this.birthday = birthday;
    this.gender = gender;
  }
}

export default Account;
